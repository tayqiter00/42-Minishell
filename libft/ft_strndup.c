#include "libft.h"

char	*ft_strndup(const char *s1, size_t n)
{
	char	*s2;

	s2 = (char *) malloc(n + 1);
	if (!s2)
		return (0);
	ft_memcpy(s2, s1, n);
	s2[n] = '\0';
	return (s2);
}
